package ModeloC;

public class Cliente {
  private int Id;
  private String Nome;
  private String End;
  private String Tel;
  
public int getId() {
	return Id;
}
public void setId(int id) {
	this.Id = id;
}
public String getNome() {
	return Nome;
}
public void setNome(String nome) {
	Nome = nome;
}
public String getEnd() {
	return End;
}
public void setEnd(String end) {
	End = end;
}
public String getTel() {
	return Tel;
}
public void setTel(String tel) {
	Tel = tel;
}
 
}
